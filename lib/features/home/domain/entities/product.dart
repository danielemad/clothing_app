class Product {
  final String id;
  final String name;
  final String description;
  final double price;
  final String imageUrl;
  final String color;
  final String size;
  final String category;

  const Product({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.imageUrl,
    required this.color,
    required this.size,
    required this.category,
  });
} 